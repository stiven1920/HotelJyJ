    private void jtablePedidoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtablePedidoMouseClicked
        // TODO add your handling code here:
        int clic = jtablePedido.rowAtPoint(evt.getPoint());
        int idPedido = (int) jtablePedido.getValueAt(clic, 0);
        String nombre = "" + jtablePedido.getValueAt(clic, 1);
        int catidad = (int) jtablePedido.getValueAt(clic, 2);
        int valor = (int) jtablePedido.getValueAt(clic, 3);

        jtxCantidad.setText(String.valueOf(catidad));
        jtxIdProducto.setText(String.valueOf(idPedido));
        jtxNombre.setText(nombre);
        jtxFoto.setText("");
        jtxValor.setText(String.valueOf(valor));
    }//GEN-LAST:event_jtablePedidoMouseClicked

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(jtxIdProducto.getText());
        String nombre = jtxNombre.getText();
        int cantidad = Integer.parseInt(jtxCantidad.getText());
        int valor = Integer.parseInt(jtxValor.getText());

        File file = new File(jtxFoto.getText());

        if (file != null) {
            controlPedido.modificarPedido(nombre, cantidad, valor, file, id);
            listarPedido();
            refrescar();
        } else {
            Pedido pedido = new Pedido(id,nombre, cantidad, valor);
            controlPedido.modificarPedidoFoto(pedido);
            listarPedido();
            refrescar();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFotoActionPerformed
        // TODO add your handling code here:
        JFileChooser j = new JFileChooser();
        FileNameExtensionFilter fil = new FileNameExtensionFilter("JPG, PNG & GIF", "jpg", "png", "gif");
        j.setFileFilter(fil);
        int s = j.showOpenDialog(this);
        if (s == JFileChooser.APPROVE_OPTION) {
            String ruta = j.getSelectedFile().getAbsolutePath();
            jtxFoto.setText(ruta);
        }
    }//GEN-LAST:event_btnFotoActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(jtxIdProducto.getText());
        int a = JOptionPane.showConfirmDialog(null, "Desea eliminar este producto", "Mensaje", JOptionPane.OK_CANCEL_OPTION);
        if (a == 0) {
            controlPedido.eliminarPedido(id);
            refrescar();
        }
        listarPedido();
    }//GEN-LAST:event_btnEliminarActionPerformed

