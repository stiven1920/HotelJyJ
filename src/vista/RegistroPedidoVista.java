    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (jtxIdProducto.getText().isEmpty() || jtxNombre.getText().isEmpty() || jtxCantidad.getText().isEmpty() || jtxValor.getText().isEmpty() || jtxFoto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe de llenar todos los campos vacios", "Â¡AVISO!", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            int id = Integer.parseInt(jtxIdProducto.getText());
            String nombre = jtxNombre.getText();
            int cantidad = Integer.parseInt(jtxCantidad.getText());
            int valor = Integer.parseInt(jtxValor.getText());
            try {
                File file = new File(jtxFoto.getText());
                Pedido pedido = new Pedido(id,nombre, cantidad, valor);
                controlPedido.guardarPedido(pedido, file);
                JOptionPane.showMessageDialog(null, " se agrego correctamente");

                refrescar();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, " no se puede agregar");

            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

