    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(jtxNumero.getText());
        String nombre = jtxTipo.getText();
        int valor = Integer.parseInt(jtxValor.getText());
        String estado = jtxEstado.getText();
        String descripcion = jtxDescripcion.getText();
        try {
            File file = new File(jtxUrl.getText());
            Habitacion habi = new Habitacion(id, nombre, valor, estado, descripcion);
            controlHabitacion.guardarHabitacion(habi, file);
            listarHabitacion();
            JOptionPane.showMessageDialog(null, " se agrego correctamente");
            refrescar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, " no se puede agregar");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

