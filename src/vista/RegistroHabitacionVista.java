    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(jtxNumero.getText());
        int a = JOptionPane.showConfirmDialog(null, "Desea eliminar este producto", "Mensaje", JOptionPane.OK_CANCEL_OPTION);
        if (a == 0) {
            controlHabitacion.eliminarHabitacion(id);
            refrescar();
        }
        listarHabitacion();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int codigo = Integer.parseInt(jtxNumero.getText());
        String tipoHabitacion = jtxTipo.getText();
        int valor = Integer.parseInt(jtxValor.getText());
        String estado = jtxEstado.getText();
        String descripcion = jtxDescripcion.getText();
        File file = new File(jtxUrl.getText());

        if (file != null) {
            controlHabitacion.modificarHabitacionFoto(tipoHabitacion, valor, estado, descripcion, file, codigo);
            listarHabitacion();
            refrescar();
        } else {
            controlHabitacion.modificarHabitacion(tipoHabitacion, valor, estado, descripcion, codigo);
            listarHabitacion();
            refrescar();
        }

    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFotoActionPerformed
        // TODO add your handling code here:
        JFileChooser j = new JFileChooser();
        FileNameExtensionFilter fil = new FileNameExtensionFilter("JPG, PNG & GIF", "jpg", "png", "gif");
        j.setFileFilter(fil);
        int s = j.showOpenDialog(this);
        if (s == JFileChooser.APPROVE_OPTION) {
            String ruta = j.getSelectedFile().getAbsolutePath();
            jtxUrl.setText(ruta);
        }
    }//GEN-LAST:event_btnFotoActionPerformed

    private void jtableHabitacionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtableHabitacionMouseClicked
        // TODO add your handling code here:
        int clic = jtableHabitacion.rowAtPoint(evt.getPoint());
        int codigo = (int) jtableHabitacion.getValueAt(clic, 0);
        String tipoHabitacion = "" + jtableHabitacion.getValueAt(clic, 1);
        int valor = (int) jtableHabitacion.getValueAt(clic, 2);
        String estado = "" + jtableHabitacion.getValueAt(clic, 3);
        String descripcion = "" + jtableHabitacion.getValueAt(clic, 4);

        jtxDescripcion.setText(descripcion);
        jtxEstado.setText(estado);
        jtxNumero.setText(String.valueOf(codigo));
        jtxTipo.setText(tipoHabitacion);
        jtxUrl.setText("");
        jtxValor.setText(String.valueOf(valor));

    }//GEN-LAST:event_jtableHabitacionMouseClicked

