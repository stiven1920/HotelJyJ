package vista;

import java.awt.Component;
import java.awt.Graphics;
import javax.swing.JDesktopPane;

/**
 *
 * @author MI PC
 */
public class Panel extends JDesktopPane {
    public Panel (){
        super();
    }
    @Override
    public void paint (Graphics g){
        super.paint(g);
        for (Component component : getComponents()) {
            component.repaint();
        }
    }
    
}
