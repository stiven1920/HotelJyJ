package modelo;

import java.sql.Date;

/**
 *
 * @author Jopcion
 */
public class ConfirmarReserva {
    
     //creamos los atributos necesarios para la clase ConfirmarReserva haciendo el debido mapeo
    private int idConfirmarReserva;
    private String nombre;
    private String apellido;
    private Date fechaLlegada;
    private Date fechInicio;
    private Date fechaSalida;
    private int reservaId;
    private int recepcionistaId;

    public ConfirmarReserva(){}
    
    //creamos el constructor

    public ConfirmarReserva(int idConfirmarReserva, String nombre, String apellido, Date fechaLlegada, Date fechInicio, Date fechaSalida, int reservaId, int recepcionistaId) {
        this.idConfirmarReserva = idConfirmarReserva;
        this.nombre = nombre;
        this.apellido = apellido;
        this.fechaLlegada = fechaLlegada;
        this.fechInicio = fechInicio;
        this.fechaSalida = fechaSalida;
        this.reservaId = reservaId;
        this.recepcionistaId = recepcionistaId;
    }

    //luego sus respectivos setters y getters
    public int getIdConfirmarReserva() {
        return idConfirmarReserva;
    }

    public void setIdConfirmarReserva(int idConfirmarReserva) {
        this.idConfirmarReserva = idConfirmarReserva;
    }

    public Date getFechaLlegada() {
        return fechaLlegada;
    }

    public void setFechaLlegada(Date fechaLlegada) {
        this.fechaLlegada = fechaLlegada;
    }

    public Date getFechaSalida() {
        return fechaSalida;
    }

    public void setFechaSalida(Date fechaSalida) {
        this.fechaSalida = fechaSalida;
    }

    public int getReservaId() {
        return reservaId;
    }

    public void setReservaId(int reservaId) {
        this.reservaId = reservaId;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public Date getFechInicio() {
        return fechInicio;
    }

    public void setFechInicio(Date fechInicio) {
        this.fechInicio = fechInicio;
    }

    public int getRecepcionistaId() {
        return recepcionistaId;
    }

    public void setRecepcionistaId(int recepcionistaId) {
        this.recepcionistaId = recepcionistaId;
    }

    @Override
    public String toString() {
        return "ConfirmarReserva{" + "idConfirmarReserva=" + idConfirmarReserva + ", nombre=" + nombre + ", apellido=" + apellido + ", fechaLlegada=" + fechaLlegada + ", fechInicio=" + fechInicio + ", fechaSalida=" + fechaSalida + ", reservaId=" + reservaId + ", recepcionistaId=" + recepcionistaId + '}';
    }

    
}
