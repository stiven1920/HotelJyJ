package modelo;

import java.sql.Date;

/**
 *
 * @author Jopcion
 */
public class Reserva {
    //creamos los atributos necesarios para la clase Reserva haciendo el debido mapeo
    private int idReserva;
    private String nombreHuesped;
    private String apellidoHuesped;
    private Date fechaInicio;
    private Date fechaSalida;
    private int huespedId;
    private int habitacionId;
    private String estado;

    public Reserva(){}
    
    //creamos el constructor

    public Reserva(int idReserva, String nombreHuesped, String apellidoHuesped, Date fechaInicio, Date fechaSalida, int huespedId, int habitacionId) {
        this.idReserva = idReserva;
        this.nombreHuesped = nombreHuesped;
        this.apellidoHuesped = apellidoHuesped;
        this.fechaInicio = fechaInicio;
        this.fechaSalida = fechaSalida;
        this.huespedId = huespedId;
        this.habitacionId = habitacionId;
    }
  
    
    //luego sus respectivos setters y getters
    public int getIdReserva() {
        return idReserva;
    }

    public void setIdReserva(int idReserva) {
        this.idReserva = idReserva;
    }

    public Date getFechaSalida() {
        return fechaSalida;
    }

    public void setFechaSalida(Date fechaSalida) {
        this.fechaSalida = fechaSalida;
    }

    public int getHuespedId() {
        return huespedId;
    }

    public void setHuespedId(int huespedId) {
        this.huespedId = huespedId;
    }

    public int getHabitacionId() {
        return habitacionId;
    }

    public void setHabitacionId(int habitacionId) {
        this.habitacionId = habitacionId;
    }

    public String getNombreHuesped() {
        return nombreHuesped;
    }

    public void setNombreHuesped(String nombreHuesped) {
        this.nombreHuesped = nombreHuesped;
    }

    public String getApellidoHuesped() {
        return apellidoHuesped;
    }

    public void setApellidoHuesped(String apellidoHuesped) {
        this.apellidoHuesped = apellidoHuesped;
    }

    public Date getFechaInicio() {
        return fechaInicio;
    }

    public void setFechaInicio(Date fechaInicio) {
        this.fechaInicio = fechaInicio;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
}
