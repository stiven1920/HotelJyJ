package modelo;

/**
 *
 * @author Jopcion
 */
public class Recepcionista {
    //creamos los atributos necesarios para la clase Recepcionista haciendo el debido mapeo
    private int codigo;
    private String nombre;
    private String apellido;
    private int telefono;
    private String direccion;
    private String usuario;
    private String contrasenia; 
    
     public Recepcionista(){
         
     }
    
    //creamos el constructor
    public Recepcionista(int codigo, String nombre, String apellido, int telefono, String direccion, String usuario, String contrasenia) {
        this.codigo = codigo;
        this.nombre = nombre;
        this.apellido = apellido;
        this.telefono = telefono;
        this.direccion = direccion;
        this.usuario = usuario;
        this.contrasenia = contrasenia;
    }
    //luego sus respectivos setters y getters
    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public int getTelefono() {
        return telefono;
    }

    public void setTelefono(int telefono) {
        this.telefono = telefono;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getContrasenia() {
        return contrasenia;
    }

    public void setContrasenia(String contrasenia) {
        this.contrasenia = contrasenia;
    }
    
}
